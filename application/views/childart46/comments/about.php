				<li>集體創作：(作者三～四人)。</li>
			<ul><h2>規格</h2></ul>
				<li>個別創作40x55公分以內形狀不拘，可依畫題內容自行決定畫紙形狀。</li>
				<li>集體創作(全開紙為限)。</li>
				<li>水墨畫以能裱於四開圖畫紙上為限(並請裱妥)。</li>
			<ul><h2>內容</h2></ul>
				<li>不拘，但必須是兒童本人的創作品，為使兒童體驗各種表現素材，請少用彩色筆作畫。</li>
			
