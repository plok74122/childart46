		<h1 class="block">徵集計劃</h1>
		依 據：中華民國第四十六屆世界兒童畫展國內作品比賽徵集簡章。<br>
		主 旨：為慶祝建國104年暨提高兒童美術教育水準，促進國際文化交流、增進人類友誼和瞭解。<br>
		指導單位：教育部、外交部、文化部、台北市政府教育局、高雄市政府教育局、中華民國兒童美術教育學會、中華民國造型藝術教育學會。<br>
		主辦單位：桃園市政府教育局。<br>
		承辦單位：中壢國民小學。<br>
		徵集對象：本市公私立國民中、小學在籍學生及幼兒園學生。<br>
		</div>
